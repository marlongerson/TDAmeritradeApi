namespace TDAmeritradeApi
{
    /// <summary>
    /// Specifies the periods to show for price history.
    /// </summary>
    public enum PeriodType
    {
        /// <summary>
        /// Represents the daily period.
        /// </summary>
        Day,
        /// <summary>
        /// Represents the monthly period.
        /// </summary>
        Month,
        /// <summary>
        /// Represents the yearly period.
        /// </summary>
        Year,
        /// <summary>
        /// Represents the year to date (YTD) period.
        /// </summary>
        YearToDate,
    }
}